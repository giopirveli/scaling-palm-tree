function searchAnime() {
  const searchInput = document
    .getElementById("searchInput")
    .value.toLowerCase();
  const results = animeCards.filter((card) =>
    card.name.toLowerCase().includes(searchInput)
  );
  if (searchInput === "") {
    let resultsDiv = document.getElementById("animecardsBox");
    resultsDiv.innerHTML = ``;
  } else {
    displayResults(results);
  }
}

function displayResults(cards) {
  const resultsDiv = document.getElementById("animecardsBox");
  resultsDiv.innerHTML = "";

  if (cards.length === 0) {
    resultsDiv.innerHTML = "<p id='animeIsnotFound'>Sorry We Dont This Anime</p>";
    return;
  }
  cards.forEach((card) => {
    resultsDiv.innerHTML += `

<div class="cardOfSerchSize">
            <div class="cardOfsearch card">
               <a href=""><img class="animeCard" src="${card.image}" alt=""></a>
               <p class="roboto-regular">${card.name}</p>

            </div>

            
        `;
  });
}


